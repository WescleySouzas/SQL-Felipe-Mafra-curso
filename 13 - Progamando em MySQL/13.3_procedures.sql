CREATE PROCEDURE CONSULTAR_CURSO(P_IDCURSO INT(30))
BEGIN

    SELECT NOME, HORAS, VALOR FROM CURSOS
    WHERE IDCURSO = P_IDCURSO 
    ORDER BY VALOR;

